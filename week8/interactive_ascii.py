def draw_face(mood):
    if mood == "happy":
        print(":-)")
    elif mood == "sad":
        print(":-(")
    elif mood == "angry":
        print(">:-(")
    elif mood == "surprised":
        print(":-O")
    elif mood == "confused":
        print(":-S")
    else:
        print(":-(")
draw_face("happy")
draw_face("sad")
draw_face("angry")
draw_face("surprised")
draw_face("confused")
draw_face("confused")